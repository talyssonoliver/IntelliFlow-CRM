/**
 * Material Symbols Icon Helper for Project Tracker
 *
 * Provides a consistent way to use Material Symbols icons throughout the app.
 * Replaces Lucide React icons with the design system standard.
 */

import { cn } from './utils';

export interface IconProps {
  /** Material Symbol icon name (e.g., 'check', 'close', 'search') */
  name: string;
  /** Additional CSS classes */
  className?: string;
  /** Size variant */
  size?: 'xs' | 'sm' | 'base' | 'lg' | 'xl' | '2xl';
}

const SIZE_CLASSES = {
  xs: 'text-xs', // 12px
  sm: 'text-sm', // 14px
  base: 'text-base', // 16px
  lg: 'text-lg', // 18px
  xl: 'text-xl', // 20px
  '2xl': 'text-2xl', // 24px
} as const;

/**
 * Icon component using Material Symbols Outlined
 *
 * @example
 * ```tsx
 * <Icon name="check" size="base" className="text-green-500" />
 * <Icon name="close" />
 * <Icon name="settings" size="lg" />
 * ```
 */
export function Icon({ name, className, size = 'base' }: IconProps) {
  return (
    <span
      className={cn('material-symbols-outlined', SIZE_CLASSES[size], className)}
      aria-hidden="true"
    >
      {name}
    </span>
  );
}

