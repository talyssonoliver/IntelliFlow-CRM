/**
 * IFC-033 — Sprint-19 critical-path load test (ADR-018 budget).
 *
 * Exercises the Sprint-19 investment-gate critical paths against a LOCAL,
 * prod-safe target (apps/api on :4000, dev-auth fallback → seeded tenant),
 * and validates the ADR-018 / IFC-033 budget:
 *
 *     throughput >= 5,000 leads/hour, p95 < 200 ms, error rate < 0.1 %.
 *
 * Critical paths covered (real tRPC calls, no mocks):
 *   - lead.create          — lead ingestion (the "5,000 leads/hour" headline)
 *   - lead.list / getById  — tRPC read critical paths
 *   - lead.convertToDeal    — the DDD-001/002 transactionally-atomic
 *                             lead → contact + account + opportunity write (#621)
 *
 * Tenant isolation (RLS) is NOT asserted here: the local test DB connects as the
 * `postgres` SUPERUSER, which bypasses Postgres RLS by design. RLS enforcement is
 * verified separately at the DB layer (non-superuser role + tenant GUC) — see
 * docs/shared/bottleneck-analysis.md § RLS. That proof shows foreign-tenant rows
 * are invisible (own=18, foreign=0), i.e. the policy tenant_isolation_leads holds
 * under a production-like role.
 *
 * Usage:
 *   BASE_URL=http://localhost:4000 \
 *   K6_WEB_DASHBOARD=true K6_WEB_DASHBOARD_EXPORT=artifacts/reports/load-test-report.html \
 *   k6 run --out experimental-prometheus-rw tools/scripts/k6/ifc-033-critical-path.js
 *
 * Or via the wrapper:  pnpm test:load
 *
 * @task IFC-033 (Sprint 19), ADR-018, gates IFC-034 (£3000 Gate-3 review)
 */
import http from 'k6/http';
import { check, sleep } from 'k6';
import { Rate, Trend, Counter } from 'k6/metrics';
import exec from 'k6/execution';

const BASE_URL = __ENV.BASE_URL || 'http://localhost:4000';
const TRPC = `${BASE_URL}/api/trpc`;

// ADR-018 budget: >=5,000 leads/hour (1.4 creates/s). We ingest at 6 creates/s
// (~21,600 leads/hour, 4.3x the SLO) to prove headroom. This is intentionally kept
// UNDER the per-user AUTHENTICATED rate-limit tier (1,000 req/60s ~= 16.7 req/s,
// keyed by userId — see apps/api/src/middleware/rate-limit.ts): the dev-auth
// fallback funnels ALL synthetic load through ONE user, so a single VU-pool would
// otherwise saturate that one user's budget. Real 5,000 leads/hour comes from many
// distinct users, each with its own budget. Driving a single user above ~16.7 req/s
// is the identified bottleneck (see docs/shared/bottleneck-analysis.md).
const INGEST_RPS = Number(__ENV.INGEST_RPS || 6); // 6 create/s + 6 read/s
const CONVERT_RPS = Number(__ENV.CONVERT_RPS || 1);
const DURATION = __ENV.DURATION || '3m';

// A known, seeded, same-tenant lead id (Sarah Miller, QUALIFIED) for getById reads.
const SEED_LEAD_ID = __ENV.SEED_LEAD_ID || '00000000-0000-4000-8000-000000000201';

// Pre-seeded pool of QUALIFIED lead ids for the conversion path (gitignored,
// generated by the run wrapper). Each id is converted at most once.
let QUALIFIED_POOL = [];
try {
  QUALIFIED_POOL = JSON.parse(open('./.data/qualified-leads.json')) || [];
} catch (_e) {
  QUALIFIED_POOL = [];
}

const errorRate = new Rate('errors');
const leadsCreated = new Counter('leads_created');
const dealsConverted = new Counter('deals_converted');
const createLatency = new Trend('lead_create_latency', true);
const readLatency = new Trend('lead_read_latency', true);
const convertLatency = new Trend('lead_convert_latency', true);

const MUT_HEADERS = {
  'Content-Type': 'application/json',
  // Custom anti-CSRF header satisfies assertMutationCsrfSafe without an
  // Authorization header (so the dev-auth fallback stays active). See apps/api/src/trpc.ts.
  'x-csrf-token': 'k6-load-test',
  Origin: BASE_URL,
};

function trpcGet(proc, input) {
  const q = encodeURIComponent(JSON.stringify(input));
  return http.get(`${TRPC}/${proc}?input=${q}`, { tags: { name: proc } });
}
function trpcPost(proc, input) {
  return http.post(`${TRPC}/${proc}`, JSON.stringify(input), {
    headers: MUT_HEADERS,
    tags: { name: proc },
  });
}

export const options = {
  scenarios: {
    // Lead ingestion + reads — the ADR-018 SLO scenario.
    lead_ingestion: {
      executor: 'constant-arrival-rate',
      rate: INGEST_RPS,
      timeUnit: '1s',
      duration: DURATION,
      preAllocatedVUs: Math.max(20, INGEST_RPS * 4),
      maxVUs: Math.max(100, INGEST_RPS * 20),
      exec: 'ingest',
    },
    // Transactional lead→deal conversion (DDD-001/002 atomic path).
    lead_conversion: {
      executor: 'constant-arrival-rate',
      rate: CONVERT_RPS,
      timeUnit: '1s',
      duration: DURATION,
      preAllocatedVUs: Math.max(10, CONVERT_RPS * 5),
      maxVUs: Math.max(40, CONVERT_RPS * 20),
      exec: 'convert',
    },
  },
  thresholds: {
    // ADR-018 / IFC-033 hard budget (evaluated on the ingestion+read SLO paths).
    'http_req_duration{scenario:lead_ingestion}': ['p(95)<200'],
    http_req_failed: ['rate<0.001'],
    errors: ['rate<0.001'],
    lead_create_latency: ['p(95)<200'],
    lead_read_latency: ['p(95)<200'],
  },
};

export function setup() {
  const res = http.get(`${BASE_URL}/health`);
  if (res.status !== 200) {
    throw new Error(`API not healthy at ${BASE_URL}/health (status ${res.status})`);
  }
  return { poolSize: QUALIFIED_POOL.length };
}

// Ingestion + read mix: one create + one read per iteration.
export function ingest() {
  const rnd = `${__VU}-${exec.scenario.iterationInTest}-${Math.floor(Math.random() * 1e9)}`;
  const createRes = trpcPost('lead.create', {
    email: `k6-ingest-${rnd}@loadtest.example.com`,
    firstName: 'K6',
    lastName: `Ingest${__VU}`,
    company: 'LoadTest Co',
    source: 'WEBSITE',
    estimatedValue: 25000,
  });
  createLatency.add(createRes.timings.duration);
  const createdOk = check(createRes, {
    'lead.create 2xx': (r) => r.status >= 200 && r.status < 300,
  });
  if (createdOk) leadsCreated.add(1);
  else errorRate.add(1);

  // Alternate the two read critical paths.
  const useList = exec.scenario.iterationInTest % 2 === 0;
  const readRes = useList
    ? trpcGet('lead.list', { page: 1, limit: 20 })
    : trpcGet('lead.getById', { id: SEED_LEAD_ID });
  readLatency.add(readRes.timings.duration);
  const readOk = check(readRes, {
    'lead read 2xx': (r) => r.status >= 200 && r.status < 300,
  });
  if (!readOk) errorRate.add(1);
}

// Transactional conversion: convert one pre-qualified pool lead (at most once).
export function convert() {
  const idx = exec.scenario.iterationInTest;
  if (idx >= QUALIFIED_POOL.length) {
    return; // pool exhausted — stop issuing conversions rather than double-convert
  }
  const leadId = QUALIFIED_POOL[idx];
  const res = trpcPost('lead.convertToDeal', {
    leadId,
    dealValue: 50000,
    dealName: `K6 Load Deal ${idx}`,
    createContact: true,
  });
  convertLatency.add(res.timings.duration);
  const ok = check(res, {
    'lead.convertToDeal 2xx': (r) => r.status >= 200 && r.status < 300,
  });
  if (ok) dealsConverted.add(1);
  else errorRate.add(1);
}

function metricValue(data, name, field) {
  const m = data.metrics && data.metrics[name];
  if (!m || !m.values) return undefined;
  return m.values[field];
}

export function handleSummary(data) {
  const runMs = data.state && data.state.testRunDurationMs;
  const durSec = runMs ? runMs / 1000 : Number(String(DURATION).replace('m', '')) * 60;
  const created = metricValue(data, 'leads_created', 'count') || 0;
  const converted = metricValue(data, 'deals_converted', 'count') || 0;
  const leadsPerHour = durSec > 0 ? Math.round((created / durSec) * 3600) : 0;
  let p95Ingest = metricValue(data, 'http_req_duration{scenario:lead_ingestion}', 'p(95)');
  if (p95Ingest == null) p95Ingest = metricValue(data, 'http_req_duration', 'p(95)');
  const failRate = metricValue(data, 'http_req_failed', 'rate') || 0;

  const summary = {
    task: 'IFC-033',
    generatedAt: new Date().toISOString(),
    target: BASE_URL,
    durationSeconds: Math.round(durSec),
    budget: { throughputPerHour: 5000, p95Ms: 200, errorRatePct: 0.1 },
    achieved: {
      leadsCreated: created,
      leadsPerHour: leadsPerHour,
      dealsConverted: converted,
      p95Ms: p95Ingest != null ? Number(p95Ingest.toFixed(1)) : null,
      errorRatePct: Number((failRate * 100).toFixed(4)),
      totalRequests: metricValue(data, 'http_reqs', 'count') || 0,
    },
    pass: {
      throughput: leadsPerHour >= 5000,
      p95: p95Ingest != null && p95Ingest < 200,
      errorRate: failRate < 0.001,
    },
  };

  const lines = [
    '',
    '=== IFC-033 critical-path load test (ADR-018 budget) ===',
    `Target:        ${BASE_URL}`,
    `Duration:      ${summary.durationSeconds}s`,
    `Leads created: ${created}  (~${leadsPerHour.toLocaleString()}/hour, budget >= 5,000)`,
    `Deals converted (atomic): ${converted}`,
    `p95 latency:   ${summary.achieved.p95Ms != null ? summary.achieved.p95Ms : 'n/a'} ms   (budget < 200 ms)`,
    `Error rate:    ${summary.achieved.errorRatePct} %   (budget < 0.1 %)`,
    `PASS: throughput=${summary.pass.throughput} p95=${summary.pass.p95} errorRate=${summary.pass.errorRate}`,
    '',
  ];

  return {
    stdout: lines.join('\n'),
    'artifacts/benchmarks/ifc-033-load-summary.json': JSON.stringify(summary, null, 2),
  };
}
